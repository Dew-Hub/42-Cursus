int	ft_str_is_alpha(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if ((97 <= str[n] && str[n] <= 122) || (65 <= str[n] && str[n] <= 90))
		{
			n++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
